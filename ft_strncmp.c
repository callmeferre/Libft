#include "libft.h"

int	ft_strncmp(char	*s1, char	*s2, size_t	n)
{
	while (n != 0 && *s1 != 0 && *s2 != 0 && *s1 == *s2)
	{
		s1++;
		s2++;
		n--;
	}
	if (n)
		return ((int)(*s1 - *s2));
	else
		return (0);
}
